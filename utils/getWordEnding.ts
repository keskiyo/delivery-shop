/**
 * Возвращает правильное окончание для слова "товар" в зависимости от количества
 * 
 * Правила русского языка:
 * - 1, 21, 31... -> "товар" (пустое окончание)
 * - 2-4, 22-24, 32-34... -> "товара" (окончание "а")
 * - 5-20, 25-30, 35-40... -> "товаров" (окончание "ов")
 * 
 * @param count - Количество товаров
 * @returns Окончание для слова "товар"
 * 
 * @example
 * `${1} товар${getWordEnding(1)}`   // "1 товар"
 * `${2} товар${getWordEnding(2)}`   // "2 товара"
 * `${5} товар${getWordEnding(5)}`   // "5 товаров"
 * `${21} товар${getWordEnding(21)}` // "21 товар"
 */
export function getWordEnding(count: number): string {
	if (count % 10 === 1 && count % 100 !== 11) {
		return ''
	} else if (
		[2, 3, 4].includes(count % 10) &&
		![12, 13, 14].includes(count % 100)
	) {
		return 'а'
	} else {
		return 'ов'
	}
}
