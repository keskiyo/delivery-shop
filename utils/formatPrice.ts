/**
 * Форматирует цену для отображения в российском формате
 * 
 * Преобразует число в строку с двумя знаками после запятой
 * и заменяет точку на запятую (российский стандарт)
 * 
 * @param price - Цена в числовом формате
 * @returns Отформатированная строка цены
 * 
 * @example
 * formatPrice(1234.5) // "1234,50"
 * formatPrice(99.99)  // "99,99"
 */
export const formatPrice = (price: number): string => {
	return price.toFixed(2).replace('.', ',')
}
